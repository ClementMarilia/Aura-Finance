const STORAGE_KEY = "crelith_language";
const SUPPORTED = ["pt", "it", "en", "es"];

export const LANGUAGES = [
  { value: "pt", label: "Português", locale: "pt-BR" },
  { value: "it", label: "Italiano", locale: "it-IT" },
  { value: "en", label: "English", locale: "en-US" },
  { value: "es", label: "Español", locale: "es-ES" },
];

function normalizeLanguage(value) {
  const short = String(value || "").toLowerCase().split("-")[0];
  return SUPPORTED.includes(short) ? short : "pt";
}

function detectLanguage() {
  const saved = localStorage.getItem(STORAGE_KEY);
  if (saved) return normalizeLanguage(saved);
  return normalizeLanguage(navigator.languages?.[0] || navigator.language);
}

let currentLanguage = detectLanguage();

export function getLanguage() {
  return currentLanguage;
}

export function getLocale() {
  return LANGUAGES.find((item) => item.value === currentLanguage)?.locale || "pt-BR";
}

export function getMonthNames(style = "short") {
  return Array.from({ length: 12 }, (_, month) => (
    new Intl.DateTimeFormat(getLocale(), { month: style }).format(new Date(2026, month, 1))
  ));
}

export function setLanguage(language, { reload = true } = {}) {
  const normalized = normalizeLanguage(language);
  localStorage.setItem(STORAGE_KEY, normalized);
  currentLanguage = normalized;
  document.documentElement.lang = normalized;
  if (reload) window.location.reload();
}

export function syncLanguage(language) {
  if (!language) return false;
  const normalized = normalizeLanguage(language);
  if (normalized === currentLanguage) return false;
  localStorage.setItem(STORAGE_KEY, normalized);
  currentLanguage = normalized;
  document.documentElement.lang = normalized;
  return true;
}

